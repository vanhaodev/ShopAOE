function tixiu_checkResult(value){
    if(value === 1)
    {
        return 'Tí';
    }
    else if(value === 2)
    {
        return 'Sửu';
    }
    else if(value === 3)
    {
        return 'Dần';
    }
    else if(value === 4)
    {
        return 'Mão';
    }
    else if(value === 5)
    {
        return 'Thìn';
    }
    else if(value === 6)
    {
        return 'Tỵ';
    }
    else if(value === 7)
    {
        return 'Ngọ';
    }
    else if(value === 8)
    {
        return 'Mùi';
    }
    else if(value === 9)
    {
        return 'Thân';
    }
    else if(value === 10)
    {
        return 'Dậu';
    }
    else if(value === 11)
    {
        return 'Tuất';
    }
    else if(value === 12)
    {
        return 'Hợi';
    }
    return 'Mày F12 à thằng loz???';
}

module.exports = tixiu_checkResult;